function love.conf(t)
    t.window.title = "덱헨즈 게임"        -- 창 제목 (원하는 제목으로 변경 가능)
    t.window.width = 2560          -- 창 너비 (21:9 비율에 맞춰 설정)
    t.window.height = 1080         -- 창 높이 (21:9 비율에 맞춰 설정)
    t.window.fullscreen = false      -- 전체화면 모드 활성화
    t.window.resizable = false     -- 창 크기 조절 비활성화 (선택 사항)
    t.window.borderless = false    -- 창 테두리 표시 (false: 테두리 없음, true: 테두리 있음)
end